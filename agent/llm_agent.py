"""
Vision-enhanced LLM analysis for skin disease predictions.

Uses a 3-step pipeline:
  1. Open-ended vision diagnosis (LLaMA 4 Scout sees the actual image)
  2. Internet search (Tavily searches for the identified condition)
  3. Combined analysis (LLM synthesizes classifier + vision + search + patient data)

This handles rare conditions that the classifier's fixed label set cannot predict.
"""

